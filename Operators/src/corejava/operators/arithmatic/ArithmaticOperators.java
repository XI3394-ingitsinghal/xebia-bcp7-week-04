package corejava.operators.arithmatic;

public class ArithmaticOperators {

	public static void main(String args[]) {
		int a = 10, b = 12;
//		* 
		System.out.println(a + b);
//		/ 
		System.out.println(a / b);
//		%
		System.out.println(a % b);
//		+
		System.out.println(a + b);
//		-
		System.out.println(a - b);

	}

}
